# complex_calc.py: 複素数型の計算
a = 1.2 + 3.4j # 1.2 + 3.4 i
b = complex(2.3, -4.5) # 2.3 - 4.5 i
print('a = ', a)
print('b = ', b)
print('i = ', 1j, ', i^2 = ', 1j ** 2)

# 四則演算
print('a + b = ', a + b)
print('a - b = ', a - b)
print('a * b = ', a * b)
print('a / b = ', a / b)

